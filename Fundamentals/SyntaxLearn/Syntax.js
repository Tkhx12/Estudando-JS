// JavaScript tem dois tipos de sintaxes de valor diferentes sendo elas
// Valores fixados e Valores variáveis
// Valores fixados são chamados de Literais
// E valores variáveis são chamados de Variáveis

// Valores Literais no JavaScript
// Mais importante para regra de sintaxe para valores fixos são
// Números que são escritos com ou sem decimal
10.50
1001
// Strings são textos, escrito entre aspas duplas ou simples "", ''
'Hello World!'
"Hello World!"

// Na linguagem de programação, variáveis são usadas pra guardar Dados
// JavaScript usa as variáveis var, let e const para declarar variáveis
// o sinal de igual (=) é usado para adicionar valores para variáveis
// no exemplo abaixo, o X é definido como uma variável e X recebe o valor 7

let j; 
j = 7;

// JavaScript usa operadores aritméticos (+ - * /) para computar valores

( 5 + 6 ) * 10

// Neste cálculo acima temos, 5 MAIS 6 VEZES 10
// Se usa um sinal de atribuição, sinal de igual ( = ) para assinalar um valor à variáveis

let k, w, c;
k = 5;
w = 7;
c = "cálculo 2"
let o = k + w
console.log(o, c)

// Expressões em JavaScript

// Uma expressão é combinada de Valores, Variáveis e Operadores que calcula um valor
// o calculo é chamado de avaliação
// por exemplo 5 * 10 é avaliada para 50

5 * 10 

// Expressões podem conter valores variáveis

x * 10

// Os valores podem ser de vários tipos, como Números e Strings
// Por exemplo "John" + "" + "Doe", vai ser retornado "John Doe"

"John" + "" + "Doe"

// JavaScript Keywords/Palavras-Chave

// As palavras-Chave no JavaScript são usadas para identificar ações para serem performadas
// a palavra chave Let diz ao navegador para criar novas variáveis

let q, l;
q = 5 + 5;
l = x * 10;

// A palavra chave Var também diz ao navegar para criar novas variáveis

var u, f;
u = 5 + 5;
l = x * 10;

// as palavras chaves Let e Var produzem o mesmo resultado.